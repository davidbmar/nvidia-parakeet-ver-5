  api_key_required: ${API_KEY_REQUIRED:-false}
  api_key_header: "${API_KEY_HEADER:-X-API-Key}"
  allowed_origins: ${ALLOWED_ORIGINS:-[]}
  max_session_duration: ${MAX_SESSION_DURATION:-3600}  # 1 hour

